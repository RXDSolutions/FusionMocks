namespace Sophis.Windows.Integration
{
    public class WindowKey
    {
        public WindowKey(int dialogType, int key, int subKey)
        {
            DialogType = dialogType;
            Key = key;
            subKey = SubKey;
        }

        public int DialogType { get; set; }

        public int Key { get; set; }

        public int SubKey { get; set; }
    }
}